  let locale = "pl";

  function t(key, params = {}) {
    let text = MESSAGES[locale]?.[key] ?? MESSAGES.pl[key] ?? key;
    Object.entries(params).forEach(([k, v]) => {
      text = text.replaceAll(`{${k}}`, String(v));
    });
    return text;
  }

  function pluralAccounts(count) {
    if (locale === "en") return count === 1 ? t("plural.account.one") : t("plural.account.few");
    if (count === 1) return t("plural.account.one");
    const mod10 = count % 10;
    const mod100 = count % 100;
    if (mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)) return t("plural.account.few");
    return t("plural.account.many");
  }

  function intlLocale() {
    return locale === "en" ? "en-US" : "pl-PL";
  }

  function setLocale(next) {
    if (!SUPPORTED.includes(next)) return;
    locale = next;
    document.documentElement.lang = next;
    document.title = t("app.title");
    applyI18n();
    global.dispatchEvent(new CustomEvent("librewallet:locale", { detail: { locale: next } }));
  }

  function getLocale() {
    return locale;
  }

  function applyI18n(root = document) {
    root.querySelectorAll("[data-i18n]").forEach((el) => {
      el.textContent = t(el.dataset.i18n);
    });
    root.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      el.placeholder = t(el.dataset.i18nPlaceholder);
    });
    root.querySelectorAll("[data-i18n-aria]").forEach((el) => {
      el.setAttribute("aria-label", t(el.dataset.i18nAria));
    });
    root.querySelectorAll("[data-i18n-title]").forEach((el) => {
      el.title = t(el.dataset.i18nTitle);
    });
    const scopeBtn = document.querySelector('.scope-button[data-portfolio-id="all"] span:last-child');
    if (scopeBtn) scopeBtn.textContent = t("nav.allInvestments");
  }

  async function detectDefaultLocale() {
    try {
      const response = await fetch("/api/default-locale");
      if (!response.ok) return null;
      const data = await response.json();
      return SUPPORTED.includes(data.locale) ? data.locale : null;
    } catch {
      return null;
    }
  }

  global.LW_I18N = {
    t,
    setLocale,
    getLocale,
    applyI18n,
    intlLocale,
    pluralAccounts,
    detectDefaultLocale,
    SUPPORTED,
    LOCALE_FILE,
  };
